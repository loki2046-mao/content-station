/**
 * 切口分析 Prompt
 * 输入一个话题/选题，输出6个不同的写作切口方向
 */

export function buildAnalyzePrompt(topic: string): string {
  return `你是一个资深的公众号内容策划，擅长从一个话题中找到不同的写作切口。

我给你一个话题，请你从6个完全不同的角度给出写作切口建议。

话题：${topic}

要求：
1. 6个切口要有明显区分度，不能重复或相近
2. 切口要适合公众号传播，有阅读吸引力
3. 考虑不同的读者心理：好奇心、共鸣、实用价值、情绪触动、认知刷新、社交谈资
4. 写人话，不要AI味道

请严格按以下JSON格式输出，不要输出其他内容：

\`\`\`json
[
  {
    "id": "1",
    "name": "切口名称（4-8个字）",
    "description": "一句话说明这个切口怎么写",
    "suitability": "high/medium/low",
    "suitabilityReason": "为什么适合/不太适合",
    "articleType": "观点文/体验文/教程文/故事文/盘点文/深度分析",
    "expandPoints": [
      "展开点1：具体可以写什么",
      "展开点2：具体可以写什么",
      "展开点3：具体可以写什么"
    ]
  }
]
\`\`\`

注意：
- suitability 的 high 意味着这个切口发出来大概率有不错的阅读量
- expandPoints 给3-5个，每个要具体，不是泛泛而谈
- 不要所有切口都是"深度分析"，要有类型多样性`;
}

/** 切口分析的系统提示词 */
export const ANALYZE_SYSTEM_PROMPT =
  "你是一个经验丰富的公众号内容策划师，了解中文互联网的传播规律，擅长找到话题的不同写作角度。你的建议要实操、接地气，不说正确的废话。";
