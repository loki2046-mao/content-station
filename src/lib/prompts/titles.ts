/**
 * 标题生成 Prompt
 * 输入选题+切口+上下文，输出3-5套完整标题方案
 */

export function buildTitlesPrompt(params: {
  topic: string;
  angle?: string; // 切口方向
  context?: string; // 补充上下文（大纲/摘要等）
}): string {
  const { topic, angle, context } = params;

  let prompt = `你是一个公众号标题高手，深谙标题对阅读量的影响。

请为以下内容生成 4 套完整的标题方案。

选题：${topic}`;

  if (angle) {
    prompt += `\n写作切口：${angle}`;
  }
  if (context) {
    prompt += `\n补充信息：${context}`;
  }

  prompt += `

每套方案包含四个层级：
- 主标题：公众号推文标题，要有传播感。标注风格类型。
- 副标题：出现在摘要位置，补充主标题信息，让人更想点进来
- 封面大字：公众号封面图上的大字，6个字以内，要有视觉冲击力
- 朋友圈导语：转发到朋友圈时配的文字，口语化，让人想点开

标题风格类型参考：好奇心型、情绪共鸣型、观点输出型、反常识型、轻梗型、深度感型

请严格按以下JSON格式输出，不要输出其他内容：

\`\`\`json
[
  {
    "id": "1",
    "mainTitle": "主标题文字",
    "style": "好奇心型",
    "subtitle": "副标题文字",
    "coverText": "封面大字",
    "shareText": "朋友圈导语文字"
  }
]
\`\`\`

要求：
1. 4套方案的风格要有差异，不能都是同一种路数
2. 标题不要营销号感（不用震惊体、不用过度夸张）
3. 标题要自然、像一个真人会写的
4. 封面大字要短而有力，适合做在封面图上
5. 朋友圈导语要口语化，像真人发朋友圈
6. 至少包含一个适合"深度内容"的标题方案`;

  return prompt;
}

/** 标题生成的系统提示词 */
export const TITLES_SYSTEM_PROMPT =
  "你是一个精通公众号标题艺术的内容创作者。你知道好标题不是靠套路而是靠洞察，了解什么样的标题能在朋友圈被点开。你的标题风格：不low、不油、不营销号，但有传播力。";
